
class Guild:

    def __init__(self, id, name, coins, created, exp, description, preferredGames, tag, members):

        self.ID = id
        self.NAME = name
        self.COINS = coins
        self.CREATED = created
        self.EXP = exp
        self.DESCRIPTION = description
        self.PREFERRED_GAMES = preferredGames
        self.TAG = tag
        self.MEMBERS = members
